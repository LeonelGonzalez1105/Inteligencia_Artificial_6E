
# NÚMEROS (enteros y decimales)
entero = 10          # literal entero
decimal = 3.14       # literal de punto flotante (decimal)
negativo = -5        # literal entero negativo

# CADENAS (texto)
saludo = "Hola mundo"    # literal de cadena con comillas dobles
nombre = 'Leonel'        # literal de cadena con comillas simples

# BOOLEANOS (True o False)
es_mayor = True          # literal booleano verdadero
es_menor = False         # literal booleano falso

# NINGÚN VALOR (None)
nada = None              # literal especial que significa "sin valor"

# LISTAS (colección ordenada y mutable)
frutas = ["manzana", "banana", "cereza"]  # lista literal
numeros = [1, 2, 3, 4]                    # lista literal de números

# TUPLAS (colección ordenada e inmutable)
coordenadas = (10, 20)     # tupla literal con dos valores
colores = ("rojo", "verde", "azul")  # tupla literal con tres cadenas

# CONJUNTOS (set) (colección sin orden y sin duplicados)
conjunto_numeros = {1, 2, 3, 3}  # literal set (duplicados se eliminan automáticamente)

# DICCIONARIOS (clave: valor)
persona = {
    "nombre": "Leonel",
    "edad": 20,
    "estudiante": True
}  # literal diccionario

# Imprimir los valores para verlos en pantalla
print("Entero:", entero)
print("Decimal:", decimal)
print("Negativo:", negativo)
print("Saludo:", saludo)
print("Nombre:", nombre)
print("Es mayor:", es_mayor)
print("Es menor:", es_menor)
print("Nada:", nada)
print("Frutas:", frutas)
print("Números:", numeros)
print("Coordenadas:", coordenadas)
print("Colores:", colores)
print("Conjunto de números:", conjunto_numeros)
print("Persona:", persona)
